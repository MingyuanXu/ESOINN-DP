def List2str(inlist,divstr=' '):
    outstr=''
    for i in inlist:
        outstr.join(str(i)+divstr)
    return outstr
def Dumpobj(obj,filename):
    file=open(filename,'wb')
    pickle.dump(obj,file)
    file.close()
    return

